/**
 * Starlight sidebar configuration.
 *
 * Kept in a standalone module so both `astro.config.mjs` and the
 * `scripts/check-sidebar.mjs` orphan-detector can import it.
 */

/**
 * @param {string} slug
 * @param {string | undefined} label
 * @returns {any}
 */
export function enterpriseEntry(slug, label = undefined) {
  return {
    link: `${slug}/`,
    label: label ?? slug,
    attrs: { class: "enterprise" },
    badge: {
      text: "React Admin Enterprise",
      variant: "default",
    },
  };
}

/**
 * @param {string} name
 * @param {string | undefined} label
 * @returns {any}
 */
export function raCoreEntry(name, label = undefined) {
  return {
    link: `https://marmelab.com/ra-core/${name.toLowerCase()}/`,
    label: label ?? name,
    attrs: { class: "ra-core", target: "_blank", rel: "noreferrer" },
    badge: {
      text: "RA Core",
      variant: "default",
    },
  };
}

